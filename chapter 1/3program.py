# Operators in python
# An operators is a symbols that preforms a certain operation between operands.

# Arithmetic operators (+,-,*,/,%**)
a = 5
b = 2

print(a + b)
print(a - b)
print(a / b)
print(a % b)
print(a ** b)

# Relational and comparison operators ( ==, !=, >, <, >=, <= )
print(a == b)
print(a != b)
print(a >= b)
print(a > b)
print(a < b)

# Assignment operators (=, +=, -=, *=, /=, %=, **= )
num = 10
num = num + 10
print(num) 

# Logical opeators (not, and , or )
print(not False)
print (not (a > b))
